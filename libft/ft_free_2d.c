#include <libft.h>

char	**ft_free_2d(char **v, size_t size)
{
	size_t	e;

	if (v)
	{
		e = 0;
		while (e < size)
		{
			ft_free(v[e]);
			e++;
		}
		ft_free(v);
	}
	return (NULL);
}

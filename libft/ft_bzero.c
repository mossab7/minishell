#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;

	if (!n)
		return ;
	ptr = s;
	while (1)
	{
		ptr[n - 1] = 0;
		if (n == 1)
			break ;
		n--;
	}
}
